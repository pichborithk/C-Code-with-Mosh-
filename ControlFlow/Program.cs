// If-Else Statement
int number = 10;
if (number > 5)
{
    Console.WriteLine("Number is greater than 5");
}
else
{
    Console.WriteLine("Number is not greater than 5");
}

// Switch Statement
char grade = 'B';
switch (grade)
{
    case 'A':
        Console.WriteLine("Excellent!");
        break;
    case 'B':
        Console.WriteLine("Good!");
        break;
    case 'C':
        Console.WriteLine("Fair!");
        break;
    default:
        Console.WriteLine("Needs improvement!");
        break;
}
